
public class CalExample02 {

	public static void main (String[] args) {
		
		byte x = 3; // 1 byte =8 bit
		            // 0000 0000
		            // 0000 0011
		
		System.out.println
		("3은 2진수로" +Integer.toBinaryString((int)x));
		
		
		System.out.println(Integer.toBinaryString(-1));
		
		// 10진수 15를 2진수로 변환
		 System.out.println(Integer.toBinaryString(15)); //= 0000 1111
		
		 // 10진수 -4를 2진수로 변환
	     System.out.println(Integer.toBinaryString(-4));//= 111111100
		
	     
		
		
		
		
	}
}
